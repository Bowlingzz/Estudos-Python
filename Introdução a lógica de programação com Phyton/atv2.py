#3- Escreva um programa que leia um valor inteiro e apresente os resultados do quadrado e do cubo do valor lido.
# ** operador matemático para potenciação
valor = int(input("digite o valor inteiro: "))
quadrado = valor ** 2
cubo = valor ** 3
print(f"o valor do quadrado é {quadrado} e o valor do cubo é {cubo}")

